// 17_5-2 While loop, debug and understand while loop

// Even number
var roastGiven = 0;

while (roastGiven < 7) {
    console.log('Roast Den, Please!!!');
    roastGiven++;
    console.log(roastGiven);
}


//Odd number
var roastGiven = 1;

while (roastGiven < 9) {
    console.log('Want to 2 pieces roast');
    roastGiven = roastGiven + 2;
    console.log(roastGiven);
}